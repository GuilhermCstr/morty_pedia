class CharacterModel {
  final int id;
  final String name;
  final String status;
  final String species;
  final String image;
  final String origin;
  final String gender;

  CharacterModel({
    required this.id,
    required this.name,
    required this.status,
    required this.species,
    required this.image,
    required this.origin,
    required this.gender,
  });
}
